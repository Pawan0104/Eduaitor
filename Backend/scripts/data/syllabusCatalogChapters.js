/**
 * Resolve chapters for a catalog book.
 * @returns {{ name: string, topics: string[] }[]}
 */
export function getChaptersForBook({ className, subjectName, title }) {
  const cls = String(className);
  const exact = BY_CLASS_TITLE[`${cls}|${title}`];
  if (exact?.length) return exact;

  // Strip board suffix for RBSE titles like "Mathematics Class 6 (RBSE)"
  const baseTitle = title
    .replace(/\s*\(RBSE\)\s*$/i, "")
    .replace(/\s*\(CBSE\)\s*$/i, "")
    .trim();

  // Map "Mathematics Class 6" → look up NCERT "Mathematics" for that class
  const classSuffix = new RegExp(`\\s+Class\\s+${cls}\\s*$`, "i");
  const withoutClass = baseTitle.replace(classSuffix, "").trim();

  for (const candidate of [baseTitle, withoutClass, title]) {
    const hit = BY_CLASS_TITLE[`${cls}|${candidate}`];
    if (hit?.length) return hit;
  }

  // Subject-level NCERT books often titled exactly as subject
  const subject = normalizeSubject(subjectName);
  const bySubjectTitle = BY_CLASS_TITLE[`${cls}|${subject}`];
  if (bySubjectTitle?.length) return bySubjectTitle;

  // Hindi medium RBSE: Ganit → Mathematics chapters for class
  if (BY_CLASS_TITLE[`${cls}|Mathematics`] && /ganit|mathematics/i.test(title + subject)) {
    return BY_CLASS_TITLE[`${cls}|Mathematics`];
  }
  if (BY_CLASS_TITLE[`${cls}|Science`] && /vigyan|science/i.test(title + subject)) {
    return BY_CLASS_TITLE[`${cls}|Science`];
  }

  const defaults = SUBJECT_DEFAULTS[subject] || SUBJECT_DEFAULTS[normalizeSubject(subject)];
  if (defaults?.length) return defaults;

  // Last resort: numbered chapters
  return chapters(
    "Chapter 1",
    "Chapter 2",
    "Chapter 3",
    "Chapter 4",
    "Chapter 5",
    "Chapter 6",
  );
}

