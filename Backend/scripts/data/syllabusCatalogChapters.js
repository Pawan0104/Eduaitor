/**
 * Chapter + topic lists for global syllabus catalog books.
 * Lookup order: class|title → class|subject → subject default → generic.
 */

const topicObj = (name, content) => ({
  name,
  content:
    content ||
    [
      `Main content: ${name}`,
      ``,
      `This topic is part of the prescribed textbook syllabus.`,
      ``,
      `What to study:`,
      `• Read the textbook section titled "${name}"`,
      `• Note important definitions, formulas, facts and examples`,
      `• Solve in-text questions and end exercises for this topic`,
      `• Revise key points before class tests / exams`,
    ].join("\n"),
});

const buildChapterMainContent = (name, topicNames = []) => {
  const outline = topicNames.length
    ? topicNames.map((t, i) => `${i + 1}. ${t}`).join("\n")
    : "1. Introduction\n2. Key concepts\n3. Exercises";

  return [
    `Main content: ${name}`,
    ``,
    `This chapter belongs to the board textbook syllabus and must be taught as per the prescribed book.`,
    ``,
    `Chapter summary:`,
    `Students learn the concepts of "${name}" through textbook explanation, examples, activities and exercises.`,
    ``,
    `Learning outcomes:`,
    `• Understand the main ideas of ${name}`,
    `• Explain key terms and concepts from the chapter`,
    `• Solve textbook examples and exercise questions`,
    `• Apply learning to related classroom / exam questions`,
    ``,
    `Topic outline:`,
    outline,
    ``,
    `Teaching note: Use the textbook as primary source. Add class notes, worksheets or PDF references as needed.`,
  ].join("\n");
};

const ch = (name, topics = ["Introduction", "Key concepts", "Exercises"]) => {
  const topicList = (topics || []).map((t) =>
    typeof t === "string" ? t : t.name,
  );
  const mainContent = buildChapterMainContent(name, topicList);
  return {
    name,
    description: `Chapter: ${name}`,
    content: mainContent,
    topics: (topics || []).map((t) =>
      typeof t === "string" ? topicObj(t) : topicObj(t.name, t.content),
    ),
  };
};

const chapters = (...names) =>
  names.map((n) => (typeof n === "string" ? ch(n) : n));

